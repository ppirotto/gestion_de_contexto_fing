package edu.fing.commons.front.dto;

public class RuleTO {

	private long id;
	private String name;
	private String drl;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDrl() {
		return drl;
	}
	public void setDrl(String drl) {
		this.drl = drl;
	}
}
